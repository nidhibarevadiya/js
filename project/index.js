const display = (products) => {

  let temp = " ";

  for (let i = 0; i < products.length; i++) {
    temp += `
  <div class="col-lg-4 col-md-6 py-3 ">
    <div class="box  ">
       <div class="flex"> <img src=${products[i].image} alt="" class="img-fluid im"></div>
       <div class="flex"> <h3 class="ti">${products[i].title}</h3></div>
        <div class="flex"> <p class="des">${products[i].description}</p></div>
       <div class="flex"> <p  class="ti1">${products[i].price}</p></div>
        <div class="flex"> <p  class="ti1">${products[i].category}</p></div>
        <div class="flex"> <p  class="ti3">${products[i].rating.rate}</p></div>
     
         <div class="flex"><button class="btn btn-success ti2" onclick="addToCart(${products[i].id})">Add to Cart</button></div>
    </div>
</div>
    `;
  }

  document.getElementById('product').innerHTML = temp;
}




const handleSort = (orderby) => {
  if (orderby === "lth") {
    products.sort((a, b) => a.price - b.price);
  } else if (orderby === "htl") {
    products.sort((a, b) => b.price - a.price);
  }

  display(products);
};

display(products);

document
  .getElementById("htl")
  .addEventListener("click", () => handleSort("htl"));
document
  .getElementById("lth")
  .addEventListener("click", () => handleSort("lth"));



const handlefilter = (category) => {
  if (category == "All") {
    display(products);
    return;
  }
  let temp = products.filter((ele) => ele.category == category);
  display(temp);
};


document
  .getElementById("brakfast")
  .addEventListener("click", () => handlefilter("Brackfast"));
document
  .getElementById("lunch")
  .addEventListener("click", () => handlefilter("Lunch"));
document
  .getElementById("dinner")
  .addEventListener("click", () => handlefilter("Dinner"));

document
  .getElementById("all")
  .addEventListener("click", () => handlefilter("All"));



const Serching = (value) => {
  let temp = products.filter((ele) =>
    ele.title.toLowerCase().includes(value.toLowerCase())
  );
  display(temp);
};

document.getElementById("search").addEventListener("input", () => {
  let value = document.getElementById("search").value;
  Serching(value);
});


let cart = JSON.parse(localStorage.getItem("cart")) || [];

const addToCart = (id) => {
  let selectedProduct = products.find((product) => product.id === id);
  let existingItem = cart.find((item) => item.id === id);

  if (existingItem) {
    existingItem.quantity += 1;
  } else {
    cart.push({ ...selectedProduct, quantity: 1 });
  }

  localStorage.setItem("cart", JSON.stringify(cart));
  alert(`${selectedProduct.title} added to cart!`);
};




